// Declare a variable 'marks' and assign value 75
let marks = 75;

// Start decision making
// Check if marks are 80 or more
if (marks >= 80) {
    // If true, print top grade
    console.log("A+");

} else if (marks >= 32) {
    // If first condition is false but marks are 32 or more
    // Then student has passed
    console.log("Passed");

} else {
    // If all above conditions are false
    // Student has failed
    console.log("Failed");
}